#!/bin/bash

OS_MIDOKURA_REPOSITORY_USER="${1}"
OS_MIDOKURA_REPOSITORY_PASS="${2}"

CONTROLLERS="${3}"
COMPUTE="${4}"
GATEWAYS="${5}"
NSDB="${6}"

cat <<EOF

MEM USER: ${OS_MIDOKURA_REPOSITORY_USER}
MEM PASS: ${OS_MIDOKURA_REPOSITORY_PASS}

RUNNING SCRIPT ${0} on $(hostname) ($(hostname -f))

CONTROLLER: ${CONTROLLERS}
COMPUTE: ${COMPUTE}
GATEWAYS: ${GATEWAYS}
NSDB: ${NSDB}

EOF

exit 0

